public class patternDemo {
    public static void main(String[] args) {
        int row = 1;
        while(row<=3)
        {
            int col=1;
            while(col <=3)
            {
                System.out.println(col+" ");
                col ++;
            }
            System.out.println();
            row ++;
        }
    }
    
}
